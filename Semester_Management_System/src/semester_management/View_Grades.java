    public void fillCombo() {
    try {
        con1 = DriverManager.getConnection("jdbc:mysql://localhost/school_db", "root", "");
        String query = "SELECT GradeID FROM grade";
        Statement st = con1.createStatement();
        rs = st.executeQuery(query);
        while (rs.next()) {
            int GradeID = rs.getInt("GradeID");
            combograde.addItem(String.valueOf(GradeID));
        }
        query = "SELECT StudentID FROM grade";
        st = con1.createStatement();
        rs = st.executeQuery(query);
        while (rs.next()) {
            int StudentID = rs.getInt("StudentID");
            combostudent.addItem(String.valueOf(StudentID));
        }
       
        query = "SELECT SubjectID FROM grade";
        st = con1.createStatement();
        rs = st.executeQuery(query);
        while (rs.next()) {
            int SubjectID = rs.getInt("SubjectID");
            combosubject.addItem(String.valueOf(SubjectID));
        }
    } catch (SQLException ex) {
        Logger.getLogger(View_Grades.class.getName()).log(Level.SEVERE, null, ex);
    } finally {
        try {
            con1.close();
            rs.close();
        } catch (SQLException ex) {
            Logger.getLogger(View_Grades.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
}
