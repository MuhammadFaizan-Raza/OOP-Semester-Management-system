    private void ViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ViewActionPerformed
        // TODO add your handling code here:
        try {
            pst = con1.prepareStatement("select GradeID,SubjectID,StudentID,Grade,GradeDate from grade where GradeID=?");
            int GradeID = Integer.parseInt(combograde.getSelectedItem().toString());
            pst.setInt(1,GradeID);
            ResultSet rs1 =  pst.executeQuery();
            if(rs1.next()==false) {
                JOptionPane.showMessageDialog(this,"Sorry Grade ID not found");
                txtsubject.setText("");

                txtstudent.setText("");
                txtgrade.setText("");
                gradedate.setDate(null);
                

                combograde.requestFocus();
            }
            else
            {
                txtgradeid.setText(rs1.getString("GradeID"));
                txtsubject.setText(rs1.getString("SubjectID"));
                txtstudent.setText(rs1.getString("StudentID"));
                txtgrade.setText(rs1.getString("Grade"));
                gradedate.setDate(rs1.getDate("GradeDate"));
                

            }
        }

        catch (SQLException ex) {
            Logger.getLogger(Student_registration.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_ViewActionPerformed

