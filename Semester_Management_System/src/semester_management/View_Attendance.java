    private void ViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ViewActionPerformed
        // TODO add your handling code here:
        try {
            pst = con1.prepareStatement("select AttendanceID,StudentID,ClassID,AttendanceDate,AttendanceStatus from attendance where AttendanceID=?");
            int AttendanceID = Integer.parseInt(comboattendance.getSelectedItem().toString());
            pst.setInt(1,AttendanceID);
            ResultSet rs1 =  pst.executeQuery();
            if(rs1.next()==false) {
                JOptionPane.showMessageDialog(this,"Sorry Attendance ID not found");
                txtstudent.setText("");
                
                txtclass.setText("");
                dob.setDate(null);
                txtattendance.setText("");
                
                comboattendance.requestFocus();
                }
            else 
            {
                txtstudent.setText(rs1.getString("StudentID"));
                txtclass.setText(rs1.getString("ClassID"));
                dob.setDate(rs1.getDate("AttendanceDate"));
                txtattendance.setText(rs1.getString("AttendanceStatus"));
                
                
            }
        }
        
        catch (SQLException ex) {
            Logger.getLogger(Student_registration.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_ViewActionPerformed

