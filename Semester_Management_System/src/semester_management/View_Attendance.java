    public void fillCombo() {
    try {
        con1 = DriverManager.getConnection("jdbc:mysql://localhost/school_db", "root", "");
        String query = "SELECT AttendanceID FROM attendance";
        Statement st = con1.createStatement();
        rs = st.executeQuery(query);
        while (rs.next()) {
            int AttendanceID = rs.getInt("AttendanceID");
            comboattendance.addItem(String.valueOf(AttendanceID));
        }
    } catch (SQLException ex) {
        Logger.getLogger(View_Attendance.class.getName()).log(Level.SEVERE, null, ex);
    } finally {
        try {
            con1.close();
            rs.close();
        } catch (SQLException ex) {
            Logger.getLogger(View_Attendance.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
}
