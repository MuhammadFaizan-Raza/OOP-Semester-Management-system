public void fillCombo() {
    try {
        con = DriverManager.getConnection("jdbc:mysql://localhost/school_db", "root", "");
        String query = "SELECT TeacherID FROM teacher";
        Statement st = con.createStatement();
        rs = st.executeQuery(query);
        while (rs.next()) {
            int TeacherID = rs.getInt("TeacherID");
            studentid5.addItem(String.valueOf(TeacherID));
        }
    } catch (SQLException ex) {
        Logger.getLogger(View_teacher.class.getName()).log(Level.SEVERE, null, ex);
    } finally {
        try {
            con.close();
            rs.close();
        } catch (SQLException ex) {
            Logger.getLogger(View_teacher.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
}

