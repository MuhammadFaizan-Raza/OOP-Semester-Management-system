    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        try {
            abc = con.prepareStatement("select TeacherID,FullName,Gender,DateOfBirth,PreferSubject,ExperienceYears,Email,ContactNumber,LastQualification from teacher where TeacherID=?");
            int StudentID = Integer.parseInt(studentid5.getSelectedItem().toString());
            abc.setInt(1,StudentID);
            ResultSet rs1 =  abc.executeQuery();
            if(rs1.next()==false) {
                JOptionPane.showMessageDialog(this,"Sorry record not found");
                txtstudentname.setText("");
                dob.setDate(null);
                combogender.setSelectedItem("");
                txtcontact.setText("");
                txtparentname.setText("");
                txtparentcontact.setText("");
                jspin.setValue(0);
                studentid5.requestFocus();
            }
            else
            {
                txtstudentname.setText(rs1.getString("FullName"));
                combogender.setSelectedItem(rs1.getString("Gender"));
                dob.setDate(rs1.getDate("DateOfBirth"));
                jspin.setValue(rs1.getInt("ExperienceYears"));
                txtcontact.setText(rs1.getString("ContactNumber"));
                txtparentname.setText(rs1.getString("PreferSubject"));
                txtparentcontact.setText(rs1.getString("LastQualification"));
              
            }
        }

        catch (SQLException ex) {
            Logger.getLogger(Student_registration.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton6ActionPerformed

