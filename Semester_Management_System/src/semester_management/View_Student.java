 public void fillCombo() {
    try {
        con = DriverManager.getConnection("jdbc:mysql://localhost/school_db", "root", "");
        String query = "SELECT StudentID FROM student";
        Statement st = con.createStatement();
        rs = st.executeQuery(query);
        while (rs.next()) {
            int StudentID = rs.getInt("StudentID");
            studentid.addItem(String.valueOf(StudentID));
        }
    } catch (SQLException ex) {
        Logger.getLogger(View_Student.class.getName()).log(Level.SEVERE, null, ex);
    } finally {
        try {
            con.close();
            rs.close();
        } catch (SQLException ex) {
            Logger.getLogger(View_Student.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
}

