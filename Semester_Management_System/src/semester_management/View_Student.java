    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            abc = con.prepareStatement("select StudentID,FullName,DateOfBirth,Gender,Address,ContactNumber,Email,GuardianName,GuardianContact,ClassID,AdmissionDate from student where StudentID=?");
            int StudentID = Integer.parseInt(studentid.getSelectedItem().toString());
            abc.setInt(1,StudentID);
            ResultSet rs1 =  abc.executeQuery();
            if(rs1.next()==false) {
                JOptionPane.showMessageDialog(this,"Sorry record not found");
                txtstudentname.setText("");
                dob.setDate(null);
                combogender.setSelectedItem("");
                txtaddress.setText("");
                txtcontact.setText("");
                txtemail.setText("");
                txtparentname.setText("");
                txtparentcontact.setText("");
                Adddate.setDate(null);
                txtclassid.setText("");
                studentid.requestFocus();
                }
            else 
            {
                txtstudentname.setText(rs1.getString("FullName"));
                dob.setDate(rs1.getDate("DateOfBirth"));
                combogender.setSelectedItem(rs1.getString("Gender"));
                txtaddress.setText(rs1.getString("Address"));
                txtcontact.setText(rs1.getString("ContactNumber"));
                txtemail.setText(rs1.getString("Email"));
                txtparentname.setText(rs1.getString("GuardianName"));
                txtparentcontact.setText(rs1.getString("GuardianContact"));
                Adddate.setDate(rs1.getDate("AdmissionDate"));
                txtclassid.setText(rs1.getString("ClassID"));
                
            }
        }
        
        catch (SQLException ex) {
            Logger.getLogger(Student_registration.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

